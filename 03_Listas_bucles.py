"""
Podemos usar un bucle for para recorrer todos los elementos de una lista.
"""

# Lista de puntuaciones
puntuaciones = [100, 150, 200, 250, 300]

print("=== PUNTUACIONES DEL JUEGO ===")

# Recorrer la lista con for
print("--- Todas las puntuaciones ---")
for puntos in puntuaciones:
    print("Puntuaci√≥n:", puntos)
